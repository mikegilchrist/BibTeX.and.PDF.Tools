def parse_ris_file(filepath):
    """Parse a RIS file into a list of entry dicts with normalized field names.

    Uses rispy if available, otherwise falls back to bibutils subprocess.
    Returns dicts with the same field names as parse_bibtex_string().
    """
    if HAS_RISPY:
        return _parse_ris_with_rispy(filepath)
    return _parse_via_bibutils(filepath, "ris")


def _parse_ris_with_rispy(filepath):
    """Parse RIS using the rispy library."""
    with open(filepath, "r", encoding="utf-8", errors="replace") as f:
        ris_entries = rispy.load(f)

    entries = []
    for ris in ris_entries:
        d = {"entry_type": "article"}

        # Authors: rispy returns list of "Surname, Firstname" strings
        authors = ris.get("authors", ris.get("first_authors", []))
        if authors:
            d["author"] = " and ".join(authors)

        # Map RIS fields to BibTeX fields
        field_map = {
            "title": "title",
            "primary_title": "title",
            "secondary_title": "journal",
            "journal_name": "journal",
            "year": "year",
            "publication_year": "year",
            "volume": "volume",
            "start_page": "_start_page",
            "end_page": "_end_page",
            "abstract": "abstract",
            "doi": "doi",
            "notes": "pmid",  # PMID sometimes stored in notes
        }
        for ris_key, bib_key in field_map.items():
            val = ris.get(ris_key)
            if val and bib_key not in d:
                if isinstance(val, list):
                    val = "; ".join(str(v) for v in val)
                d[bib_key] = str(val)

        # Combine start/end pages
        sp = d.pop("_start_page", None)
        ep = d.pop("_end_page", None)
        if sp:
            d["pages"] = f"{sp}-{ep}" if ep else sp

        # Year extraction: rispy may return "2012/03/15" or "2012"
        if "year" in d:
            year_match = re.match(r'(\d{4})', str(d["year"]))
            if year_match:
                d["year"] = year_match.group(1)

        entries.append(d)
    return entries



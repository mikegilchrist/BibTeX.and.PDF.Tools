def parse_bibtex_string(text):
    """Parse BibTeX string into a list of entry dicts.

    Each dict has keys: entry_type, cite_key, and all BibTeX fields
    (author, title, year, journal, volume, pages, abstract, doi, pmid, etc.).

    Uses bibtexparser if available, otherwise falls back to regex parsing.
    """
    if HAS_BIBTEXPARSER:
        return _parse_with_bibtexparser(text)
    return _parse_with_regex(text)


def _parse_with_bibtexparser(text):
    """Parse BibTeX using the bibtexparser library.

    Supports both v1.x (BibTexParser class) and v2.x (parse function).
    """
    # v2 API: bibtexparser.parse()
    if hasattr(bibtexparser, 'parse'):
        library = bibtexparser.parse(text)
        entries = []
        for entry in library.entries:
            d = {"entry_type": entry.entry_type, "cite_key": entry.key}
            for field in entry.fields:
                d[field.key.lower()] = field.value
            entries.append(d)
        return entries

    # v1 API: bibtexparser.loads() with BibTexParser
    parser = bibtexparser.bparser.BibTexParser(common_strings=True)
    parser.ignore_nonstandard_types = False
    library = bibtexparser.loads(text, parser=parser)
    entries = []
    for entry in library.entries:
        d = {"entry_type": entry.get("ENTRYTYPE", "article"),
             "cite_key": entry.get("ID", "")}
        for key, value in entry.items():
            if key not in ("ENTRYTYPE", "ID"):
                d[key.lower()] = value
        entries.append(d)
    return entries


def _parse_with_regex(text):
    """Parse BibTeX using regex. Handles well-formed entries from bibutils
    and journal websites. Not a full parser -- does not handle nested braces
    in field values beyond one level."""
    entries = []
    # Match @type{key, ... }
    # Use a state machine approach for robustness with nested braces
    entry_starts = list(re.finditer(r'@(\w+)\s*\{', text))

    for idx, match in enumerate(entry_starts):
        entry_type = match.group(1).lower()
        start = match.end()

        # Find the matching closing brace by counting brace depth
        depth = 1
        pos = start
        while pos < len(text) and depth > 0:
            if text[pos] == '{':
                depth += 1
            elif text[pos] == '}':
                depth -= 1
            pos += 1
        body = text[start:pos - 1]

        # Extract cite key (everything before first comma)
        comma_pos = body.find(',')
        if comma_pos == -1:
            continue
        cite_key = body[:comma_pos].strip()
        fields_text = body[comma_pos + 1:]

        d = {"entry_type": entry_type, "cite_key": cite_key}

        # Parse fields: key = {value} or key = "value" or key = number
        field_pattern = re.compile(
            r'(\w[\w-]*)\s*=\s*'
            r'(?:\{((?:[^{}]|\{[^{}]*\})*)\}'  # {value with {nested}}
            r'|"([^"]*)"'                       # "value"
            r'|(\d+))',                          # bare number
            re.DOTALL,
        )
        for m in field_pattern.finditer(fields_text):
            key = m.group(1).lower()
            value = m.group(2) if m.group(2) is not None else (
                m.group(3) if m.group(3) is not None else m.group(4)
            )
            if value is not None:
                # Normalize whitespace
                value = re.sub(r'\s+', ' ', value).strip()
                d[key] = value

        entries.append(d)
    return entries



def process_entry(entry, refs_dir, pdf_path=None, dry_run=False,
                  verbose=False, no_move=False):
    """Process a single bibliography entry.

    Generates key and filename, optionally moves PDF.
    Returns (formatted_bibtex, key, filename) tuple.
    """
    authors = parse_authors(entry.get("author", ""))
    year = entry.get("year", "0000")
    title = entry.get("title", "")
    journal = entry.get("journal", "")

    key = generate_key(authors, year)
    filename = generate_filename(authors, year, title, journal)

    if verbose:
        print(f"  Key:      {key}")
        print(f"  Filename: {filename}")

    formatted = format_bibtex_entry(entry, key, filename, refs_dir)

    if pdf_path and not no_move:
        move_and_link_pdf(pdf_path, filename, refs_dir,
                          dry_run=dry_run, verbose=verbose)

    return formatted, key, filename



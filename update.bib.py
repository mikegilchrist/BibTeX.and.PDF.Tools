def format_bibtex_entry(entry, key, filename, refs_dir):
    """Format a single BibTeX entry with standardized field order.

    Matches the existing format in bibliography.full.bib:
    tabs for indentation, specific field ordering, file = {} field.
    Protected words in the title are wrapped in {} to prevent BibTeX
    from auto-lowercasing them.
    """
    entry_type = entry.get("entry_type", "article")
    fullpath = os.path.join(str(refs_dir), filename)

    # Get timestamp
    now = datetime.now()
    timestamp = f"{now.year}.{now.month}.{now.day}"

    lines = [f"@{entry_type}{{{key},"]

    # Field order matching existing bibliography.full.bib
    field_order = [
        ("author", "author"),
        ("title", "title"),
        ("journal", "journal"),
        ("journal-iso", "journal-iso"),
        ("keywords", "keywords"),
        ("year", "year"),
        ("volume", "volume"),
        ("pages", "pages"),
        ("abstract", "abstract"),
    ]

    # Look up journal abbreviation if not already provided
    journal_name = entry.get("journal", "")
    journal_iso = entry.get("journal-iso", "")
    if journal_name and not journal_iso:
        abbrev = _journal_db.get_abbreviation(journal_name)
        if abbrev:
            entry = dict(entry)  # avoid mutating original
            entry["journal-iso"] = abbrev
    elif journal_iso and not journal_name:
        full = _journal_db.get_full_name(journal_iso)
        if full:
            entry = dict(entry)
            entry["journal"] = full

    # Compute alignment: field names are padded, values use tab alignment
    for display_name, field_key in field_order:
        value = entry.get(field_key, entry.get(display_name, ""))
        if value is None:
            value = ""
        # Clean up "ER" artifacts from ISI conversion
        value = re.sub(r'\s*ER\s*$', '', str(value))
        # Protect case-sensitive words in title field
        if display_name == "title":
            value = protect_bibtex_title(value)
        padding = " " * max(1, 12 - len(display_name))
        lines.append(f"\t{display_name}{padding}= {{{value}}},")

    # file field
    lines.append(f"\tfile\t= {{{fullpath}:PDF}},")

    # timestamp
    lines.append(f"\ttimestamp  = {{{timestamp}}},")

    # pmid
    pmid = entry.get("pmid", "")
    if pmid:
        pmid = re.sub(r'\s*ER\s*$', '', str(pmid))
    lines.append(f"\tpmid\t   = {{{pmid}}},")

    # doi (last field, no trailing comma)
    doi = entry.get("doi", "")
    if doi:
        doi = re.sub(r'\s*ER\s*$', '', str(doi))
        lines.append(f"\tdoi\t  = {{{doi}}}")
    else:
        # Remove trailing comma from pmid line
        lines[-1] = lines[-1].rstrip(",")

    lines.append("}")
    return "\n".join(lines)



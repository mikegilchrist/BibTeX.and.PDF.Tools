# Batch mode file discovery
# ---------------------------------------------------------------------------

def find_bib_files(directory, n, verbose=False):
    """Find the N newest bibliography files in directory.

    Excludes files matching *-used* (already processed).
    Returns list of Path objects sorted by mtime (newest first).
    """
    directory = Path(directory)
    if not directory.is_dir():
        print(f"ERROR: staging directory not found: {directory}",
              file=sys.stderr)
        return []

    candidates = []
    for f in directory.iterdir():
        if not f.is_file():
            continue
        if f.suffix.lower() not in SUPPORTED_EXTENSIONS:
            continue
        if "-used" in f.name:
            continue
        candidates.append(f)

    # Sort by modification time, newest first
    candidates.sort(key=lambda p: p.stat().st_mtime, reverse=True)

    if verbose:
        print(f"Found {len(candidates)} bibliography files in {directory}")
        for c in candidates[:10]:
            print(f"  {c.name}")

    if len(candidates) < n:
        print(f"ERROR: requested {n} files but only found {len(candidates)} "
              f"in {directory}", file=sys.stderr)
        return []

    return candidates[:n]


# ---------------------------------------------------------------------------
# Mode detection
# ---------------------------------------------------------------------------

def detect_mode(args):
    """Determine operating mode from positional arguments.

    Returns: (mode, details) where mode is one of:
        "batch"  -- details is int N
        "paired" -- details is (bibfile, pdffile)
        "files"  -- details is list of file paths
    """
    if len(args) == 1:
        # Single argument: integer -> batch, file -> single file
        if args[0].isdigit():
            return ("batch", int(args[0]))
        return ("files", [args[0]])

    if len(args) == 2:
        # Two arguments: check if one is a PDF
        a, b = args
        a_is_pdf = a.lower().endswith(".pdf")
        b_is_pdf = b.lower().endswith(".pdf")

        if a_is_pdf and not b_is_pdf:
            return ("paired", (b, a))  # (bibfile, pdffile)
        if b_is_pdf and not a_is_pdf:
            return ("paired", (a, b))  # (bibfile, pdffile)
        # Neither or both are PDFs -- treat as file list
        return ("files", list(args))

    # 3+ arguments: file list mode
    return ("files", list(args))


# ---------------------------------------------------------------------------
# Dependency checking
# ---------------------------------------------------------------------------

def check_dependencies(formats_needed, verbose=False):
    """Check that required tools are available for the given formats.

    Returns True if all dependencies are met, False otherwise.
    """
    ok = True

    # Check for native Python libraries
    if "ris" in formats_needed and not HAS_RISPY:
        if not shutil.which("ris2xml") or not shutil.which("xml2bib"):
            print("ERROR: rispy not installed and bibutils (ris2xml, xml2bib) "
                  "not found. Install rispy: pip install rispy", file=sys.stderr)
            ok = False
        elif verbose:
            print("NOTE: rispy not installed, using bibutils for .ris files")

    if "bib" in formats_needed and not HAS_BIBTEXPARSER:
        if verbose:
            print("NOTE: bibtexparser not installed, using built-in parser")

    # Check bibutils for formats that always need it
    bibutils_formats = formats_needed - {"ris", "bib"}
    if ("ris" in formats_needed and not HAS_RISPY):
        bibutils_formats.add("ris")

    for fmt in bibutils_formats:
        converter = BIBUTILS_CONVERTERS.get(fmt)
        if converter and not shutil.which(converter):
            print(f"ERROR: {converter} not found (needed for .{fmt} files). "
                  f"Install bibutils.", file=sys.stderr)
            ok = False
        if not shutil.which("xml2bib"):
            print("ERROR: xml2bib not found. Install bibutils.",
                  file=sys.stderr)
            ok = False
            break

    return ok



def backup_and_prepend(new_text, bib_path, dry_run=False, verbose=False):
    """Prepend new BibTeX entries to the master bibliography file.

    Backup chain:
    1. Bkups/bibliography.full.bib -> Bkups/bibliography.full.bkup
    2. bibliography.full.bib -> Bkups/bibliography.full.bib
    3. new_text + old content -> bibliography.full.bib
    """
    bib_path = Path(bib_path)
    bkups_dir = bib_path.parent / "Bkups"

    if not bib_path.exists():
        print(f"WARNING: {bib_path} does not exist. Will create it.",
              file=sys.stderr)
        if dry_run:
            print(f"[dry-run] Would create {bib_path}")
            return
        bib_path.parent.mkdir(parents=True, exist_ok=True)
        with open(bib_path, "w", encoding="utf-8") as f:
            f.write(new_text + "\n")
        return

    if dry_run:
        print(f"[dry-run] Would prepend {len(new_text)} chars to {bib_path}")
        return

    # Create backup directory if needed
    bkups_dir.mkdir(parents=True, exist_ok=True)

    bib_name = bib_path.name
    bkup_path = bkups_dir / bib_name
    bkup2_path = bkups_dir / bib_name.replace(".bib", ".bkup")

    # Rotate backups
    if bkup_path.exists():
        shutil.copy2(str(bkup_path), str(bkup2_path))
    shutil.copy2(str(bib_path), str(bkup_path))

    # Read existing content and prepend
    with open(bib_path, "r", encoding="utf-8") as f:
        existing = f.read()
    with open(bib_path, "w", encoding="utf-8") as f:
        f.write(new_text + "\n\n" + existing)

    if verbose:
        print(f"Prepended to {bib_path} (backup in {bkups_dir})")


def move_and_link_pdf(src_pdf, dest_filename, refs_dir, dry_run=False,
                      verbose=False):
    """Copy PDF to refs_dir with standardized name, create symlink in cwd.

    Args:
        src_pdf: Path to the source PDF file
        dest_filename: Standardized filename (e.g., Smith.et.al_2012_..._Journal.pdf)
        refs_dir: Target directory (default ~/References)
        dry_run: If True, only print what would be done
        verbose: If True, print detailed output
    """
    src = Path(src_pdf)
    dest = Path(refs_dir) / dest_filename
    link = Path.cwd() / dest_filename

    if not src.exists():
        print(f"ERROR: PDF not found: {src}", file=sys.stderr)
        return False

    if dry_run:
        print(f"[dry-run] Would copy {src} -> {dest}")
        print(f"[dry-run] Would symlink {link} -> {dest}")
        return True

    # Copy PDF to references directory
    dest.parent.mkdir(parents=True, exist_ok=True)
    shutil.copy2(str(src), str(dest))
    if verbose:
        print(f"Copied {src} -> {dest}")

    # Create symlink in current directory
    if link.exists() or link.is_symlink():
        if verbose:
            print(f"Symlink already exists: {link}")
    else:
        link.symlink_to(dest)
        if verbose:
            print(f"Symlinked {link} -> {dest}")

    return True



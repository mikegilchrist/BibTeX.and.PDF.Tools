def parse_authors(author_str):
    """Parse a BibTeX author string into a list of (surname, firstnames) tuples.

    Handles formats:
        "Surname, F. and Surname2, G. H."
        "Surname, Firstname and Surname2, Firstname2"
        "F. Surname and G. Surname2"  (rare but possible)
    """
    if not author_str:
        return []

    # Split on " and " (BibTeX standard separator)
    parts = re.split(r'\s+and\s+', author_str)
    authors = []
    for part in parts:
        part = part.strip()
        if not part:
            continue
        if "," in part:
            # "Surname, Firstnames" format (most common)
            surname, _, firstnames = part.partition(",")
            surname = surname.strip()
            firstnames = firstnames.strip()
        else:
            # "Firstnames Surname" format -- last token is surname
            tokens = part.split()
            if len(tokens) >= 2:
                surname = tokens[-1]
                firstnames = " ".join(tokens[:-1])
            else:
                surname = part
                firstnames = ""
        # Title-case surname if all caps
        if surname == surname.upper() and len(surname) > 1:
            surname = _title_case_surname(surname)
        # Reduce firstnames to initials
        firstnames = _to_initials(firstnames)
        authors.append((surname, firstnames))
    return authors


def _title_case_surname(name):
    """Title-case a surname with special handling for Mc/Mac/O' prefixes."""
    name = name.title()
    # Fix Mc/Mac/O' prefixes: McDonald, MacArthur, O'Brien
    name = re.sub(r"^(Mc|Mac|O')([a-z])", lambda m: m.group(1) + m.group(2).upper(), name)
    return name


def _to_initials(firstnames):
    """Convert firstnames to initials: 'John Michael' -> 'J. M.'"""
    if not firstnames:
        return ""
    # If already initials like "J. M.", normalize spacing
    # Remove lowercase letters (keep only uppercase initials)
    initials = re.findall(r'[A-Z]', firstnames)
    if initials:
        return ". ".join(initials) + "."
    # Edge case: all lowercase (shouldn't happen, but be safe)
    words = firstnames.split()
    return ". ".join(w[0].upper() for w in words if w) + "." if words else ""


# ---------------------------------------------------------------------------
# Key and filename generation
# ---------------------------------------------------------------------------

def generate_key(authors, year):
    """Generate a BibTeX citation key from authors and year.

    1 author:   Surname + Year         -> Smith2012
    2 authors:  Surname1AndSurname2Year -> SmithAndJones2012
    3+ authors: Surname1EtAlYear       -> SmithEtAl2012
    """
    if not authors:
        return f"Unknown{year}"
    if len(authors) == 1:
        key = authors[0][0]
    elif len(authors) == 2:
        key = f"{authors[0][0]}And{authors[1][0]}"
    else:
        key = f"{authors[0][0]}EtAl"
    # Remove spaces and non-alphanumeric chars from key
    key = re.sub(r'[^A-Za-z0-9]', '', key)
    return f"{key}{year}"


def generate_filename(authors, year, title, journal):
    """Generate a standardized PDF filename.

    Format: Author(s)_YYYY_title.words_Journal.Name.pdf

    1 author:   Smith_2012_title.words_Journal.pdf
    2 authors:  Smith.and.Jones_2012_title.words_Journal.pdf
    3+ authors: Smith.et.al_2012_title.words_Journal.pdf
    """
    # Build author stem
    if not authors:
        author_stem = "Unknown"
    elif len(authors) == 1:
        author_stem = authors[0][0]
    elif len(authors) == 2:
        author_stem = f"{authors[0][0]}.and.{authors[1][0]}"
    else:
        author_stem = f"{authors[0][0]}.et.al"

    # Process title
    title = _clean_title_for_filename(title or "untitled")

    # Process journal
    journal = _clean_journal_for_filename(journal or "")

    # Assemble
    if journal:
        filename = f"{author_stem}_{year}_{title}_{journal}.pdf"
    else:
        filename = f"{author_stem}_{year}_{title}.pdf"

    # Final cleanup
    filename = _sanitize_filename(filename)
    return filename


def _clean_title_for_filename(title):
    """Clean a title for use in a filename.

    Lowercases most title words but preserves protected words like DNA,
    mRNA, CRISPR, etc. whose case is significant.
    """
    # Remove trailing period
    title = title.rstrip(".")
    # Remove apostrophes
    title = title.replace("'", "")

    # Temporarily replace protected words with placeholders to prevent
    # the lowercasing regex from touching them
    placeholders = {}
    def _protect(m):
        token = f"\x00PW{len(placeholders)}\x00"
        placeholders[token] = m.group(0)
        return token
    title = _PROTECTED_PATTERN.sub(_protect, title)

    # Lowercase words that start with a single capital letter followed by
    # a lowercase letter or space (preserves all-caps acronyms).
    # Matches the Perl: s/\b([A-Z][ a-z])/ lc($1)/eg
    title = re.sub(
        r'\b([A-Z])([ a-z])',
        lambda m: m.group(1).lower() + m.group(2),
        title
    )

    # Restore protected words
    for token, original in placeholders.items():
        title = title.replace(token, original)

    return title


def _clean_journal_for_filename(journal):
    """Clean a journal name for use in a filename."""
    if not journal:
        return ""
    # Check custom short abbreviations first (PNAS, PRSB, etc.)
    for full_name, abbrev in FILENAME_JOURNAL_ABBREVS.items():
        if journal.lower() == full_name.lower():
            return abbrev
    # Replace & with "and"
    journal = journal.replace("&", "and")
    # Fix PLoS casing
    journal = re.sub(r'^Plos\.?', 'PLoS.', journal, flags=re.IGNORECASE)
    return journal


def _sanitize_filename(filename):
    """Sanitize a filename: replace special chars, collapse separators."""
    # Replace spaces, commas, parentheses with periods
    filename = re.sub(r'[ ,()]+', '.', filename)
    # Replace colons, semicolons, slashes, question marks, exclamation
    # marks (optionally followed by a period) with hyphens
    filename = re.sub(r'[-:/;?!]+\.?', '-', filename)
    # Fix separator before underscore: -_ or ._ -> _
    filename = re.sub(r'[-._]_', '_', filename)
    # Remove any remaining non-word characters (keep . - _)
    filename = re.sub(r'[^\w._-]', '', filename)
    # Collapse multiple periods
    filename = re.sub(r'\.{2,}', '.', filename)
    # Collapse multiple hyphens
    filename = re.sub(r'-{2,}', '-', filename)
    return filename


# ---------------------------------------------------------------------------
# BibTeX title protection
# ---------------------------------------------------------------------------

def protect_bibtex_title(title):
    """Wrap protected words in {} to prevent BibTeX from lowercasing them.

    Examples:
        "DNA replication in E. coli" -> "{DNA} replication in {E.} coli"
        "Analysis of mRNA stability"  -> "Analysis of {mRNA} stability"

    Words already wrapped in braces are left alone.
    """
    if not title:
        return title

    # Protect known acronyms/terms from the PROTECTED_WORDS list
    def _wrap(m):
        word = m.group(0)
        # Check if already inside braces by looking at surrounding context
        start = m.start()
        if start > 0 and title[start - 1] == '{':
            return word  # already protected
        return '{' + word + '}'

    result = _PROTECTED_PATTERN.sub(_wrap, title)
    return result



def check_dependencies(formats_needed, verbose=False):
    """Check that required tools are available for the given formats.

    Returns True if all dependencies are met, False otherwise.
    """
    ok = True

    # Check for native Python libraries
    if "ris" in formats_needed and not HAS_RISPY:
        if not shutil.which("ris2xml") or not shutil.which("xml2bib"):
            print("ERROR: rispy not installed and bibutils (ris2xml, xml2bib) "
                  "not found. Install rispy: pip install rispy", file=sys.stderr)
            ok = False
        elif verbose:
            print("NOTE: rispy not installed, using bibutils for .ris files")

    if "bib" in formats_needed and not HAS_BIBTEXPARSER:
        if verbose:
            print("NOTE: bibtexparser not installed, using built-in parser")

    # Check bibutils for formats that always need it
    bibutils_formats = formats_needed - {"ris", "bib"}
    if ("ris" in formats_needed and not HAS_RISPY):
        bibutils_formats.add("ris")

    for fmt in bibutils_formats:
        converter = BIBUTILS_CONVERTERS.get(fmt)
        if converter and not shutil.which(converter):
            print(f"ERROR: {converter} not found (needed for .{fmt} files). "
                  f"Install bibutils.", file=sys.stderr)
            ok = False
        if not shutil.which("xml2bib"):
            print("ERROR: xml2bib not found. Install bibutils.",
                  file=sys.stderr)
            ok = False
            break

    return ok



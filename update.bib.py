def _parse_via_bibutils(filepath, fmt):
    """Convert any supported format to BibTeX via bibutils subprocess,
    then parse the resulting BibTeX."""
    converter = BIBUTILS_CONVERTERS.get(fmt)

    if fmt == "xml":
        # Already MODS XML, just pipe to xml2bib
        cmd = ["xml2bib", "-nb", filepath]
    elif converter:
        # Two-stage: format2xml | xml2bib
        cmd_str = f'{converter} "{filepath}" | xml2bib -nb'
        result = subprocess.run(
            cmd_str, shell=True, capture_output=True, text=True
        )
        if result.returncode != 0:
            print(f"ERROR: bibutils conversion failed for {filepath}",
                  file=sys.stderr)
            if result.stderr:
                print(f"  {result.stderr.strip()}", file=sys.stderr)
            return []
        return parse_bibtex_string(result.stdout)
    else:
        print(f"ERROR: no converter for format '{fmt}'", file=sys.stderr)
        return []

    result = subprocess.run(cmd, capture_output=True, text=True)
    if result.returncode != 0:
        print(f"ERROR: bibutils conversion failed for {filepath}",
              file=sys.stderr)
        return []
    return parse_bibtex_string(result.stdout)


def convert_file_to_entries(filepath, fmt=None):
    """Convert a bibliography file to a list of entry dicts.

    Auto-detects format from extension if fmt is not specified.
    Uses native Python libraries for .bib and .ris when available.
    Falls back to bibutils subprocess for all other formats.
    """
    filepath = str(filepath)
    if fmt is None:
        ext = os.path.splitext(filepath)[1].lower().lstrip(".")
    else:
        ext = fmt.lower()

    if ext == "bib":
        with open(filepath, "r", encoding="utf-8", errors="replace") as f:
            text = f.read()
        return parse_bibtex_string(text)
    elif ext == "ris":
        return parse_ris_file(filepath)
    elif ext in BIBUTILS_CONVERTERS:
        return _parse_via_bibutils(filepath, ext)
    else:
        print(f"WARNING: unrecognized format '.{ext}' for {filepath} "
              f"-- skipping", file=sys.stderr)
        return []



# Usage:        fetch.pdf.with.browser.py URL TARGET_PATH [OPTIONS]
# Input:        URL         -- starting URL to load
#               TARGET_PATH -- absolute path where the PDF should be saved
# Output:       Saved PDF at TARGET_PATH; exit 0 on success, 1 on failure
#
# Options:
#   --proxy-prefix URL   Prepend proxy URL to the initial URL
#                        (e.g. https://utk.idm.oclc.org/login?url=)
#   --timeout SECS       Auto-close after N seconds (0 = no timeout, default 120)
#   -h, --help           Show this help message

def _crossref_authors_to_bibtex(author_list):
    """Convert Crossref author list to BibTeX 'and'-separated string."""
    parts = []
    for a in author_list:
        family = a.get("family", "")
        given  = a.get("given", "")
        if family and given:
            parts.append(f"{family}, {given}")
        elif family:
            parts.append(family)
    return " and ".join(parts)


def _ncbi_author_to_bibtex(name_str):
    """Convert NCBI author name 'Gold ER' -> 'Gold, E. R.'"""
    parts = name_str.strip().split()
    if len(parts) >= 2:
        family   = parts[0]
        initials = ". ".join(list(parts[1])) + "."
        return f"{family}, {initials}"
    return name_str


def _strip_jats(text):
    """Strip JATS/XML tags from Crossref abstract."""
    return re.sub(r"<[^>]+>", "", text or "").strip()


def _fetch_json(url, headers=None):
    """GET a URL and return parsed JSON, or raise on error."""
    req = Request(url, headers=headers or {})
    with urlopen(req, timeout=10) as resp:
        return json.loads(resp.read().decode())


